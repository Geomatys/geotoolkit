@XmlAccessorType(XmlAccessType.FIELD)
@XmlType(name = "", propOrder = {
    "format"
})
@XmlRootElement(name = "Exception")
public class Exception {

    @XmlElement(name = "Format", required = true)
    private List<String> format = new ArrayList<String>();

    /**
     * An empty constructor used by JAXB.
     */
     Exception() {
     }

    /**
     * Build a new Contact person primary object.
     */
    public Exception(final String... formats) {
        for (final String element : formats) {
            this.format.add(element);
        }
    }
    /**
     * Gets the value of the format property.
     *
     */
    public List<String> getFormat() {
        return Collections.unmodifiableList(format);
    }

    @Override
    public String toString() {
        final StringBuilder s = new StringBuilder();
        if (format != null) {
            for (String ext: format) {
                s.append("format:").append(ext).append('\n');
            }
            s.append('\n');
        }
        return s.toString();
    }

    /**
     * Verifie si cette entree est identique a l'objet specifie.
     */
    @Override
    public boolean equals(final Object object) {
        if (object == this) {
            return true;
        }
        if (object instanceof Exception) {
            final Exception that = (Exception) object;

            return Objects.equals(this.format, that.format);
        }
        return false;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 61 * hash + (this.format != null ? this.format.hashCode() : 0);
        return hash;
    }


}
