package org.geotoolkit.observation.json;

import com.fasterxml.jackson.core.JsonGenerator;
import com.fasterxml.jackson.databind.JsonSerializer;
import com.fasterxml.jackson.databind.SerializerProvider;
import java.io.IOException;
import org.opengis.metadata.Identifier;

/**
 *
 * @author Guilhem Legal (geomatys)
 */
public class IdentifierSerializer extends JsonSerializer<Identifier> {

    @Override
    public void serialize(Identifier id, JsonGenerator writer, SerializerProvider serializerProvider) throws IOException {
        writer.writeString(id.getCode());
    }

}
