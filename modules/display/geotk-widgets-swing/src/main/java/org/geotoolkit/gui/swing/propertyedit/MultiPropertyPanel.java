    public boolean setSelectedPropertyPanel(final PropertyPane panel) {

        if (panel != null) {
            if (panels.contains(panel)) {
                active = panel;
                SwingUtilities.invokeLater(new Runnable() {

                    @Override
                            public void run() {
                                panprop.removeAll();
                                panprop.add(panel.getComponent());
                                panprop.revalidate();
                                panprop.repaint();
                            }
                        });

                return true;
            }
        }

        return false;
    }

