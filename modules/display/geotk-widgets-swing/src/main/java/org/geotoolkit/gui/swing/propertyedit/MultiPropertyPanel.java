    @Override
    public void setTarget(Object target) {
        for (PropertyPane pan : panels) {
            pan.setTarget(target);
        }
    }

    @Override
    public void apply() {
        for (PropertyPane pan : panels) {

            if (pan.equals(active)) {
                pan.apply();
            } else {
                pan.reset();
            }
        }
    }

    @Override
    public void reset() {
        for (PropertyPane pan : panels) {
            pan.reset();
        }
    }

